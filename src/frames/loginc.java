    private void btnmodificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnmodificarActionPerformed
        String claveant = txtclaveant.getText();
        String newpass = txtclave.getText();
        String confpas = txtconfirme.getText();
        String user = txtusuario.getText();
        
        String pass_crypto = DigestUtils.md5Hex(confpas);
        String pass_crypto2 = DigestUtils.md5Hex(newpass);
        String encriptado = DigestUtils.md5Hex(claveant);
       
        
              
        String mensaje = "";
        String cap = "", cap1= "";
        
        if(!pass_crypto2.equals(pass_crypto)){
            
            
            JOptionPane.showMessageDialog(null, "Nueva Contrase√±a no Coinciden");
            
            
            
        }else{
            
        String sql = "SELECT * FROM administrador WHERE usuario ='"+user+"' && clave='"+encriptado+"'";

        try {
            Statement st = cn.createStatement();
            ResultSet rs = st.executeQuery(sql);

            while(rs.next()){
                cap= rs.getString("usuario");
                cap1= rs.getString("clave");
            }

        
            
            if (cap.equals(user) && cap1.equals(encriptado)){
                
                String sSQL = "";
                sSQL = "UPDATE administrador SET usuario = ?, clave = ? WHERE usuario ='"+user+"' && clave='"+encriptado+"'";
                
                 mensaje = "Los datos se han Moficado correctamente";
       
       
        try {
            PreparedStatement pst = cn.prepareStatement(sSQL);
             pst.setString(1, user);
             pst.setString(2, pass_crypto);
           
            int n =pst.executeUpdate();
            
            if (n > 0){
                
                JOptionPane.showMessageDialog(null, mensaje);
           
            }
            
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, ex);
        }
                
             Editar();
            } else{
                JOptionPane.showMessageDialog(null, "Usuario o Clave invalida");
            }

        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, ex);;
        }
        
        }
    }//GEN-LAST:event_btnmodificarActionPerformed

