    private void btningresarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btningresarActionPerformed
        String user = txtusuario.getText();
         String pass = txtclave.getText();
         
         String encriptado = DigestUtils.md5Hex(pass);
         
         
        String cap = "", cap1= "";  
        String sql = "SELECT * FROM administrador WHERE usuario ='"+user+"' && clave='"+encriptado+"'"                             ; 
    
        try {
            Statement st = cn.createStatement();
            ResultSet rs = st.executeQuery(sql);
            
            while(rs.next()){
                cap= rs.getString("usuario"); 
                cap1= rs.getString("clave"); 
            }
            
            if (cap.equals(user) && cap1.equals(encriptado)){
                PantallaPrincipal pp = new PantallaPrincipal();
                pp.setVisible(true);
                this.dispose();
        } else{
            JOptionPane.showMessageDialog(null, "Usuario o Clave invalida");
            }
           
        } catch (SQLException ex) {
             JOptionPane.showMessageDialog(null, ex);;
        }
    }//GEN-LAST:event_btningresarActionPerformed

